"""
This program demonstrates the creation of variables
with different data types in Python.
"""

# Integer (int)
age = 20

# Floating-point number (float)
height = 5.9

# String (str)
name = "Alice"

# Boolean (bool)
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10.5, 20.3)

# Dictionary (dict)
person = {
    "name": "Alice",
    "age": 20,
    "is_student": True
}

# Set
unique_numbers = {1, 2, 3, 3, 2}

# Display the variables and their types
print("Age:", age, "| Type:", type(age))
print("Height:", height, "| Type:", type(height))
print("Name:", name, "| Type:", type(name))
print("Is Student:", is_student, "| Type:", type(is_student))
print("Fruits:", fruits, "| Type:", type(fruits))
print("Coordinates:", coordinates, "| Type:", type(coordinates))
print("Person:", person, "| Type:", type(person))
print("Unique Numbers:", unique_numbers, "| Type:", type(unique_numbers))
