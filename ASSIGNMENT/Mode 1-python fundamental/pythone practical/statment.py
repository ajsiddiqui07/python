# Program to check whether a number is greater than,
# less than, or equal to another number

# Taking input from the user
num = int(input("Enter a number: "))

# Comparing the number using if-else statements
if num > 10:
    print(f"{num} is greater than 10.")
elif num < 10:
    print(f"{num} is less than 10.")
else:
    print(f"{num} is equal to 10.")
